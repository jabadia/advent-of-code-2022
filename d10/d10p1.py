def solve(input):
    x = 1
    cycle = 0
    signal_strength = 0
    for instruction in input.strip().split('\n'):
        if instruction == 'noop':
            cycle += 1
            if (cycle + 20) % 40 == 0:
                signal_strength += cycle * x
        else:
            _, value = instruction.split(' ')
            assert _ == 'addx'
            value = int(value)
            cycle += 1
            if (cycle + 20) % 40 == 0:
                signal_strength += cycle * x
            cycle += 1
            if (cycle + 20) % 40 == 0:
                signal_strength += cycle * x
            x += value
        # print(cycle, x)
    return signal_strength


